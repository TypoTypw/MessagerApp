class Person {
  final String id;
  final String name;
  final String surname;
  final bool online;
  final String imgURL;

  Person(
      {required this.id,
      required this.name,
      required this.surname,
      required this.online,
      required this.imgURL});

  Person reset() {
    return Person(id: '', name: '', surname: '', online: false, imgURL: '');
  }
}
